# Task to find day from users input

import datetime

x = datetime.datetime.today()

print("Year = ",x.year)
print("Month = ",x.month)
print("Day = ",x.day)
print("week = ",x.weekday()) 
print("Hour = ",x.hour)
print("Minutes = ",x.min)
print("Seconds = ",x.second)
print("Micro Seconds = ",x.microsecond)





